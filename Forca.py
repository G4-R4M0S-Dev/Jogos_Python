def apresentacao():
    print("############################################")
    print("################JOGO DA FORCA###############")
    print("############################################")

def carregaPalavraSecreta():
    with open('frutas.txt', 'r', encoding="utf-8") as f:
        palavras = []
        for linha in f:
            linha = linha.strip().upper()
            linha = unidecode(linha)  #removendo os acentos
            palavras.append(linha)

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero]
    return palavra_secreta




def inicializaLetrasAcertadas(palavra):
    return ["_" for letra in palavra]

def marcaChuteCorreto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index += 1

